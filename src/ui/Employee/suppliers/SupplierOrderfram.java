    private void btnSeeproduct4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeeproduct4ActionPerformed
        // TODO add your handling code here:
        String supllierID = txtSupplierid.getText();

        if (supllierID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Supplier id");
            return;
        }

        SupplierDAO dao = new SupplierDAO();
        Supplier sup = dao.getProductByID(supllierID);

        if (sup != null) {
            txtSupplierName.setText(sup.getName());
            txtSupplierContact.setText(sup.getContact());
            txtSupplierAddress.setText(sup.getAddress());
            txtSupplierCompanyName.setText(sup.getCompanyName());
            txtSupplierGSTNo.setText(sup.getGSTNO());
        } else {
            JOptionPane.showMessageDialog(this, "Supplier not Extis , Add new Supplier");
            return;
        }
    }//GEN-LAST:event_btnSeeproduct4ActionPerformed

    private void btnSeeproductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeeproductActionPerformed
        // TODO add your handling code here:
        String Prodid = txtProId.getText();

        if (Prodid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill product id for product detials");
            return;
        }

        ProductDAO dao = new ProductDAO();
        Product pro = dao.getProductByID(Prodid);

        if (pro != null) {
            txtPrName.setText(pro.getName());
            txtProCategary.setText(pro.getCategary());
            txtProMetal.setText(pro.getMetal());
            txtWeight.setText(String.valueOf(pro.getWeight()));
            txtproDesign.setText(pro.getDesign());
            txtprocarate.setText(pro.getCarat());

        } else {
            JOptionPane.showMessageDialog(this, "This id prodect not found , Please add product");
            return;
        }

    }//GEN-LAST:event_btnSeeproductActionPerformed

