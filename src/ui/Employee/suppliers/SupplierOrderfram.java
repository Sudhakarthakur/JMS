    private boolean validateItems() {

        if (productFields == null || productFields.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please add at least one product");
            return false;
        }

        String supplierid = txtSupplierid.getText().trim();

        if (supplierid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Supplier ID cannot be empty");
            return false;
        }

        for (int i = 0; i < productFields.size(); i++) {

            String productid = productFields.get(i).getText().trim();
            String quantity = quantityFields.get(i).getText().trim();

            if (productid.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Product ID cannot be empty (Row " + (i + 1) + ")");
                productFields.get(i).requestFocus();
                return false;
            }

            if (quantity.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Quantity cannot be empty (Row " + (i + 1) + ")");
                quantityFields.get(i).requestFocus();
                return false;
            }

            try {
                int qty = Integer.parseInt(quantity);
                if (qty <= 0) {
                    JOptionPane.showMessageDialog(this, "Quantity must be greater than 0 (Row " + (i + 1) + ")");
                    return false;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Quantity must be a number (Row " + (i + 1) + ")");
                return false;
            }
        }

        return true;
    }
